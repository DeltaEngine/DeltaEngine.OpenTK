namespace DeltaEngine.Input
{
	/// <summary>
	/// Each left and right Thumb Stick and Trigger
	/// </summary>
	public enum GamePadAnalog
	{
		/// <summary>
		/// Left Thumb Stick on both Xbox 360 controller and PS3 controller.
		/// </summary>
		LeftThumbStick,
		/// <summary>
		/// Right Thumb Stick on both Xbox 360 controller and PS3 controller.
		/// </summary>
		RightThumbStick,
		/// <summary>
		/// Left Trigger on Xbox 360 controller, L2 on PS3 controller.
		/// </summary>
		LeftTrigger,
		/// <summary>
		/// Right Trigger on Xbox 360 controller, R2 on PS3 controller.
		/// </summary>
		RightTrigger
	}
}